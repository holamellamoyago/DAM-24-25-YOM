CREATE PROCEDURE dbo.MayorDeDosEnteros 
	(@N1 INT, @N2 INT, @MENSAJE VARCHAR(30) OUTPUT)
AS
IF (@N1 = @N2)
	SET @MENSAJE = 'Son iguales'
ELSE IF (@N1 > @N2)
	SET @MENSAJE = CAST(@N1 AS VARCHAR)
ELSE 
	SET @MENSAJE = CAST(@N2 AS VARCHAR)
	
	
