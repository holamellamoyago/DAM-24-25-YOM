IF OBJECT_ID('dbo.SUMAPOSITIVO') IS NOT NULL
	DROP PROCEDURE dbo.SUMAPOSITIVO
GO

CREATE PROCEDURE dbo.SUMAPOSITIVO
	(@N1 INT, @RESULTADO INT = 1 OUTPUT)
AS 
IF (@N1 < 1 )
	SET @RESULTADO = -1
ELSE
	BEGIN 
		DECLARE @CONTADOR INT
		SET @CONTADOR = 0
		SET @RESULTADO = 0
		
		WHILE (@CONTADOR != @N1)
		BEGIN 
			SET @RESULTADO = @CONTADOR + @RESULTADO
			SET @CONTADOR = @CONTADOR +1 
		END
		
	END
	

DECLARE @RESULTADO2 INT
EXEC dbo.SUMAPOSITIVO 3, @RESULTADO2 OUTPUT
SELECT @RESULTADO2