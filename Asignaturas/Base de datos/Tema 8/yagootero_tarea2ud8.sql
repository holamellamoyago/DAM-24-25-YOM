CREATE FUNCTION FNEJER3 
	(@N INT)
	RETURNS TABLE
AS
	RETURN (
		SELECT NomeDepartamento
		FROM DEPARTAMENTO D 
		INNER JOIN PROXECTO P ON P.NumDepartControla = D.NumDepartamento
		GROUP BY NomeDepartamento
		HAVING COUNT(P.NumProxecto) > @N
		
	)
GO
	
