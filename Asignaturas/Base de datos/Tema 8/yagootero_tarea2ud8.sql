CREATE PROCEDURE prFamiliarEdad
	(@EDAD INT)
AS
SELECT * , 
(SELECT COUNT(*)
	FROM FAMILIAR F
	WHERE DATEDIFF(DD, DataNacemento, GETDATE()) / 365.25 > @EDAD AND
		F.NSS_empregado = E.NSS
) AS NUM_FAMILIARES
FROM EMPREGADO E 

