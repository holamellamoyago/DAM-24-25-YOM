IF OBJECT_ID('FN_ESVOCAL') IS NOT NULL
	DROP FUNCTION FN_ESVOCAL

DECLARE @BOOLEANO BIT

CREATE FUNCTION FN_ESVOCAL
	(@C CHAR)
RETURNS BIT
AS 
BEGIN 
	RETURN (CASE
		WHEN LOWER(@C) IN ('a','e','i','o','u',
							'á','é','í','ó','ú') THEN 1
		ELSE 0
		END)
END

