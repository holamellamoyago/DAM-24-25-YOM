CREATE FUNCTION fnVocales 
	(@V VARCHAR(250))
RETURNS INT
BEGIN

DECLARE @CONTADOR INT
SET @CONTADOR = 0

DECLARE @CONTADORVocales INT
SET @CONTADORVocales = 0


WHILE @CONTADOR = LEN(@V)
	BEGIN
		SELECT
			@CONTADORVocales AS CONTADOR,
			CASE dbo.FN_ESVOCAL(LEFT(@V, @CONTADOR))
				WHEN 1 THEN @CONTADORVocales +1 
			ELSE @CONTADOR +1
			END AS RESULTADO
	END;
	
RETURN @CONTADORVocales
END

	
 







